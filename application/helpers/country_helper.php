function get_countries() {
    return CountryCodes::$country;
}
function get_country_name($cc) {
    return CountryCodes::$country[$cc];
}

function get_continent_name($cc) {
    return CountryCodes::$continent[$cc];
}

function group_by_continent($dojo_data) {
    $dojos = array();
    foreach($dojo_data as $dojo){
        $continent = get_continent_name($dojo->country);
        $dojos[$continent][$dojo->id] = $dojo;
    }
    return $dojos;
}

function group_by_country($dojo_data) {
    $dojos = array();
    foreach($dojo_data as $dojo){
        $country = get_country_name($dojo->country);
        $dojos[$country][$dojo->id] = $dojo;
    }
    return $dojos;
}

function group_by_continent_country($dojo_data) {
    $dojos = array();
    foreach($dojo_data as $dojo){
        $continent = get_continent_name($dojo->country);
        $country = get_country_name($dojo->country);
        $dojos[$continent][$country][$dojo->id] = $dojo;
    }
    return $dojos;
}
function count_by_continent_country($dojo_data) {
    $dojos = array();
    $used_continent = array();
    $used_country = array();
    foreach($dojo_data as $dojo){
        $continent = get_continent_name($dojo->country);
        $country = get_country_name($dojo->country);
        if(in_array($continent,$used_continent)) {
            if(in_array($country,$used_country)) {
                // Used continent, used country
                $dojos[$continent][$country] += 1;
            } else {
                // Used continent, new country
                $used_country[] = $country;
                $dojos[$continent][$country] = 1;
            }
        } else {
            // New continent, new country
            $used_continent[] = $continent;
            $used_country[] = $country;
            $dojos[$continent][$country] = 1;
        }
    }
    foreach($used_continent as $continent) {
        ksort($dojos[$continent]);
    }
    ksort($dojos);
    return $dojos;
}